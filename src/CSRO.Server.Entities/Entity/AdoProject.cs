using CSRO.Server.Infrastructure;
using System;
using System.Collections.Generic;

namespace CSRO.Server.Entities.Entity
{
    public enum ProjectState
    {
        //
        // Summary:
        //     Project has not been changed.
        Unchanged = -2,
        //
        // Summary:
        //     All projects regardless of state.
        All = -1,
        //
        // Summary:
        //     Project is in the process of being created.
        New = 0,
        //
        // Summary:
        //     Project is completely created and ready to use.
        WellFormed = 1,
        //
        // Summary:
        //     Project is in the process of being deleted.
        Deleting = 2,
        //
        // Summary:
        //     Project has been queued for creation, but the process has not yet started.
        CreatePending = 3,
        //
        // Summary:
        //     Project has been deleted.
        Deleted = 4
    }

    public enum ProjectVisibility
    {
        Unchanged = -1,
        //
        // Summary:
        //     The project is only visible to users with explicit access.
        Private = 0,
        //
        // Summary:
        //     Enterprise level project visibility
        Organization = 1,
        //
        // Summary:
        //     The project is visible to all.
        Public = 2,
        SystemPrivate = 3
    }

    public class AdoProject : EntitySoftDeleteBase
    {
        public AdoProject()
        {
            AdoProjectHistoryList = new();
        }
        public List<AdoProjectHistory> AdoProjectHistoryList { get; set; }

        public Status Status { get; set; }

        public string Organization { get; set; }
        public string Name { get; set; }
        public string Description { get; set; }
        public string ProcessName { get; set; }
        public Guid? AdoId { get; set; }
        public Guid? NamespaceId { get; set; }
        public string Url { get; set; }
        public ProjectVisibility Visibility { get; set; }
        public ProjectState State { get; set; }
    }
}
